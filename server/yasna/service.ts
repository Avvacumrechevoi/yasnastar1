function buildInspectorState(params: {
  activeYasna: YasnaDetailView;
  config: NegotiationConfig;
  selectedPoint: YasnaPointView | null;
  oppositePoint: YasnaPointView | null;
  latestMechanic: Mechanic | null;
  activeMechanics: Mechanic[];
}): YasnaInspectorState {
  const { activeYasna, config, selectedPoint, oppositePoint, latestMechanic, activeMechanics } = params;
  const inspectorMechanics = activeMechanics.map((mechanic) =>
    toInspectorMechanicView(mechanic, selectedPoint?.index ?? null, oppositePoint),
  );
  const lessonFocus = getLessonFocusForPoint(activeYasna, selectedPoint);
  const lessonPreviews = getLessonPreviewsForPoint(activeYasna, selectedPoint);

  if (!selectedPoint) {
    return {
      title: activeYasna.title,
      subtitle: `${activeYasna.family} · ${activeYasna.lessonCount} уроков`,
      primaryText: activeYasna.summary,
      contextText: getMechanicContextText(latestMechanic, null, oppositePoint),
      selectedPoint: null,
      oppositePoint: null,
      lessonFocus: null,
      lessonPreviews: [],
      activeMechanic: latestMechanic,
      activeMechanics: inspectorMechanics,
    };
  }

  const primaryText = getPointPrompt(selectedPoint, config.lens, oppositePoint);
  const subtitleParts = [`Полочка ${selectedPoint.index}`];

  if (oppositePoint) {
    subtitleParts.push(`напротив ${oppositePoint.index}`);
  }

  return {
    title: selectedPoint.label,
    subtitle: subtitleParts.join(" · "),
    primaryText,
    contextText: getMechanicContextText(latestMechanic, selectedPoint.index, oppositePoint),
    selectedPoint,
    oppositePoint,
    lessonFocus,
    lessonPreviews,
    activeMechanic: latestMechanic,
    activeMechanics: inspectorMechanics,
  };
}

function getActivePreset(config: NegotiationConfig, presetId?: string | null, analysisMode?: AnalysisMode | null) {
  const explicitPreset = config.presets.find((preset) => preset.id === presetId) ?? null;

  if (explicitPreset) {
    return explicitPreset;
  }

  if (analysisMode === "guided" || analysisMode === "synthesis") {
    return config.presets[0] ?? null;
  }

  return null;
}

function resolveActiveMechanics(params: {
  requestedMechanicIds: string[];
  config: NegotiationConfig;
  activePreset: ScenarioPreset | null;
  analysisMode: AnalysisMode;
}) {
  const { requestedMechanicIds, config, activePreset, analysisMode } = params;

  if (requestedMechanicIds.length > 0) {
    return findMechanics(requestedMechanicIds);
  }

  if (activePreset) {
    return findMechanics(activePreset.recommendedMechanicIds);
  }

  if (analysisMode !== "free") {
    return findMechanics(config.lens.defaultMechanicIds);
  }

  return [];
}

