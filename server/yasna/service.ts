type YasnaStateCacheEntry = {
  value: YasnaComputedState;
  expiresAt: number;
};

const YASNA_STATE_CACHE_TTL_MS = 5 * 60 * 1000;
const YASNA_STATE_CACHE_LIMIT = 180;
const MAX_INSPECTOR_LESSON_PREVIEWS = 2;
const yasnaStateCache = new Map<string, YasnaStateCacheEntry>();

function getYasnaFillSignature(record: YasnaRecord) {
  return Array.from({ length: 12 }, (_, index) => {
    const text = record.points[String(index)]?.trim() ?? "";
    return text ? "1" : "0";
  }).join("");
}

function buildYasnaStateCacheKey(record: YasnaRecord, input: ComputeYasnaStateInput, analysisMode: AnalysisMode) {
  const selectedPoint = typeof input.selectedPoint === "number" && input.selectedPoint >= 0 && input.selectedPoint <= 11
    ? input.selectedPoint
    : "none";
  const activeMechanicIds = uniqueIds(input.activeMechanicIds ?? []).join(",");
  const presetId = analysisMode === "guided" ? input.presetId ?? "none" : "none";
  const secondaryYasnaId = analysisMode === "synthesis" ? input.secondaryYasnaId ?? "none" : "none";

  return [
    record.id,
    getYasnaFillSignature(record),
    analysisMode,
    `point:${selectedPoint}`,
    `preset:${presetId}`,
    `secondary:${secondaryYasnaId}`,
    `mechanics:${activeMechanicIds}`,
  ].join("|");
}

function readCachedYasnaState(cacheKey: string) {
  const cached = yasnaStateCache.get(cacheKey);

  if (!cached) {
    return null;
  }

  if (cached.expiresAt <= Date.now()) {
    yasnaStateCache.delete(cacheKey);
    return null;
  }

  yasnaStateCache.delete(cacheKey);
  yasnaStateCache.set(cacheKey, cached);
  return cached.value;
}

function writeCachedYasnaState(cacheKey: string, value: YasnaComputedState) {
  yasnaStateCache.set(cacheKey, {
    value,
    expiresAt: Date.now() + YASNA_STATE_CACHE_TTL_MS,
  });

  if (yasnaStateCache.size <= YASNA_STATE_CACHE_LIMIT) {
    return value;
  }

  const oldestKey = yasnaStateCache.keys().next().value;
  if (oldestKey) {
    yasnaStateCache.delete(oldestKey);
  }

  return value;
}

