function normalizeInlineText(text?: string | null) {
  return (text ?? "").replace(/\s+/g, " ").trim();
}

function getLeadingSentence(text?: string | null) {
  const normalized = normalizeInlineText(text);

  if (!normalized) {
    return "";
  }

  const match = normalized.match(/^.+?[.!?](?=\s|$)/);

  return match?.[0]?.trim() ?? normalized;
}

function getCatalogContextAccent(record: YasnaRecord) {
  return (
    getLeadingSentence(record.notes[0]) ||
    getLeadingSentence(record.lessons[0]?.interface_notes) ||
    getLeadingSentence(record.lessons[0]?.topics) ||
    getLeadingSentence(record.summary)
  );
}

function truncate(text: string, maxLength: number) {
  if (text.length <= maxLength) {
    return text;
  }

  return `${text.slice(0, Math.max(0, maxLength - 1)).trimEnd()}…`;
}

function getPointSegments(text?: string | null) {
  return normalizeInlineText(text)
    .split("|")
    .map((segment) => segment.trim())
    .filter(Boolean);
}

function upperFirst(text: string) {
  if (!text) {
    return text;
  }

  return `${text.slice(0, 1).toUpperCase()}${text.slice(1)}`;
}

function normalizePointLabelCandidate(source: string) {
  return normalizeInlineText(source)
    .replace(/\([^)]*\)/g, " ")
    .replace(/[—–-].*$/, " ")
    .replace(/[«»"']/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function getCompactPointLabel(source: string) {
  const candidates = normalizeInlineText(source)
    .split(/[;,/]/)
    .map((candidate) => normalizePointLabelCandidate(candidate))
    .filter(Boolean);

  if (!candidates.length) {
    return "";
  }

  const preferred = [...candidates].sort((left, right) => {
    const leftWordCount = left.split(" ").filter(Boolean).length;
    const rightWordCount = right.split(" ").filter(Boolean).length;

    if (leftWordCount !== rightWordCount) {
      return leftWordCount - rightWordCount;
    }

    if (left.length !== right.length) {
      return left.length - right.length;
    }

    return candidates.indexOf(left) - candidates.indexOf(right);
  })[0];

  const words = preferred.split(" ").filter(Boolean);
  if (words.length <= 2) {
    return upperFirst(preferred);
  }

  return upperFirst(words.slice(0, 2).join(" "));
}

export function getPointLabel(text?: string | null) {
  const segments = getPointSegments(text);
  const primary = segments[0] || "";
  const compactLabel = getCompactPointLabel(primary);
  return compactLabel || upperFirst(primary) || "Полочка не заполнена";
}

export function getPointEssence(text?: string | null) {
  const segments = getPointSegments(text);
  const candidate = segments[1] || segments[0] || "";
  return truncate(candidate.replace(/;\s*/g, " · "), 220);
}

export function getPointTooltip(text?: string | null) {
  return getPointSegments(text).join("\n");
}

export function getOppositePoint(index: number) {
  return (index + 6) % 12;
}

function getPointAssignmentsMap(pointAssignments?: string | null) {
  return normalizeInlineText(pointAssignments)
    .split(",")
    .map((entry) => entry.trim())
    .filter(Boolean)
    .reduce<Map<number, string>>((map, entry) => {
      const match = entry.match(/^(\d{1,2})\s*=\s*(.+)$/);

      if (!match) {
        return map;
      }

      const pointIndex = Number(match[1]);
      const pointTitle = normalizeInlineText(match[2]);

      if (Number.isNaN(pointIndex) || pointIndex < 0 || pointIndex > 11 || !pointTitle) {
        return map;
      }

      map.set(pointIndex, pointTitle);
      return map;
    }, new Map<number, string>());
}

function getLessonFocusForPoint(activeYasna: YasnaDetailView, selectedPoint: YasnaPointView | null): YasnaLessonFocusView | null {
  if (!selectedPoint) {
    return null;
  }

  const matchedLesson = activeYasna.lessons.find((lesson) => getPointAssignmentsMap(lesson.point_assignments).has(selectedPoint.index))
    ?? activeYasna.lessons.find((lesson) => {
      return Boolean(
        normalizeInlineText(lesson.topics)
        || normalizeInlineText(lesson.interface_notes)
        || normalizeInlineText(lesson.lesson)
        || lesson.mechanics_mentions?.length,
      );
    });

  if (!matchedLesson) {
    return null;
  }

  const pointAssignments = getPointAssignmentsMap(matchedLesson.point_assignments);
  const assignedTitle = pointAssignments.get(selectedPoint.index);
  const fallbackTitle = getLeadingSentence(matchedLesson.topics)
    || getLeadingSentence(matchedLesson.lesson)
    || selectedPoint.label;
  const title = upperFirst(assignedTitle || fallbackTitle || selectedPoint.label);
  const pointDescription = assignedTitle ? `${upperFirst(assignedTitle)}.` : "";
  const lessonDescription = getLeadingSentence(matchedLesson.interface_notes)
    || getLeadingSentence(matchedLesson.topics)
    || getLeadingSentence(matchedLesson.lesson)
    || "";
  const description = [pointDescription, lessonDescription].filter(Boolean).join(" ")
    || `${selectedPoint.label} показывает, как Ясна «${activeYasna.title}» разворачивается в этой точке.`;

  return {
    pointIndex: selectedPoint.index,
    title,
    description,
    mechanicMentions: uniqueIds(matchedLesson.mechanics_mentions),
    sourceLesson: normalizeInlineText(matchedLesson.lesson) || null,
  };
}

function getLessonPreviewsForPoint(activeYasna: YasnaDetailView, selectedPoint: YasnaPointView | null): YasnaLessonPreviewView[] {
  if (!selectedPoint) {
    return [];
  }

  return activeYasna.lessons
    .map((lesson, index) => {
      const pointAssignments = getPointAssignmentsMap(lesson.point_assignments);
      const assignedTitle = pointAssignments.get(selectedPoint.index);
      const lessonTitle = getLeadingSentence(lesson.lesson);
      const pointDescription = assignedTitle ? `${upperFirst(assignedTitle)}.` : "";
      const lessonDescription = getLeadingSentence(lesson.interface_notes)
        || getLeadingSentence(lesson.topics)
        || "";
      const description = [pointDescription, lessonDescription].filter(Boolean).join(" ")
        || `${selectedPoint.label} читается через урок Ясны «${activeYasna.title}».`;
      const sourceLesson = normalizeInlineText(lesson.lesson) || null;
      const sourceFile = normalizeInlineText(lesson.file) || null;
      const title = lessonTitle || upperFirst(assignedTitle || `Урок ${index + 1}`);
      const relevance = pointAssignments.has(selectedPoint.index) ? 2 : description ? 1 : 0;

      return {
        relevance,
        preview: {
          id: `${activeYasna.id}-lesson-${index}`,
          title,
          description,
          sourceLesson,
          sourceFile,
        },
      };
    })
    .filter((item) => item.relevance > 0 && (item.preview.title || item.preview.description))
    .sort((left, right) => right.relevance - left.relevance)
    .slice(0, MAX_INSPECTOR_LESSON_PREVIEWS)
    .map((item) => item.preview);
}

