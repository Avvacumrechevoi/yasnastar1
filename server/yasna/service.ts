function getMechanicContextText(
  mechanic: Mechanic | null,
  selectedPoint: number | null,
  oppositePointView: YasnaPointView | null,
) {
  if (!mechanic) {
    return "Выберите механику слева, чтобы увидеть её смысл на выбранной полочке.";
  }

  if (selectedPoint === null) {
    return "Выберите полочку слева, чтобы увидеть, как эта механика работает в конкретной точке.";
  }

  if (mechanic.kind === "contrast") {
    if (oppositePointView) {
      return `Механика «${mechanic.title}» связывает эту полочку с противоположной ${oppositePointView.index} — ${oppositePointView.label}.`;
    }

    return `Механика «${mechanic.title}» показывает связь выбранной полочки с противоположной точкой луча.`;
  }

  const mechanicSense = truncate(mechanic.description, 180).replace(/[.!?]+$/, "");

  if (mechanic.points.includes(selectedPoint)) {
    return `Механика «${mechanic.title}» проходит через эту полочку и показывает: ${mechanicSense}.`;
  }

  return `Механика «${mechanic.title}» не проходит через эту полочку напрямую, но помогает сверить её с контуром: ${mechanicSense}.`;
}

function getHighlightedPointIndices(activeMechanics: Mechanic[], selectedPoint: number | null, oppositePoint: number | null) {
  const points = new Set<number>();

  if (selectedPoint !== null) {
    points.add(selectedPoint);
  }

  activeMechanics.forEach((mechanic) => {
    mechanic.points.forEach((point) => points.add(point));

    if (mechanic.kind === "contrast" && selectedPoint !== null) {
      points.add(selectedPoint);
      if (oppositePoint !== null) {
        points.add(oppositePoint);
      }
    }
  });

  return Array.from(points).sort((left, right) => left - right);
}

